							<h2>Source Code Repository</h2>
							To enable our developers to work from anywhere in the world and maintain a secure, backed-up versioned code base, we keep all source code in <a href="https://github.com/oddsource/" target="GitHub">GitHub</a>. Everyone has access to read the contents of the repositories, and developers can request permission to be granted committer access.<br />
							<br />
							If you have a change you want to make to our source code, it's as simple as forking one of our repositories, creating a branch, making your chance, commiting to your fork, and then submitting a pull request. Frequent contributers will eventually be granted committer rights.<br />
							<br />
							
							<span class="bold">A Note about IDEs:</span> All NWTS projects are committed to GitHub with an IntelliJ IDEA 14 directory-based project structure. The community edition of IntelliJ is FREE, but may or may not support all of the features used in any given project. Personal Ultimate licenses for IntelliJ IDEA are $199 (but we are working on getting approved for free, OpenSource project licenses for developers to use). <span class="bold">You can use <span class="italic">any</span> IDE you want to to work on NWTS projects.</span> You do not have to use IntelliJ IDEA. However, to keep from polluting the repositories with several different project structures, please do not commit the project structure for any other version of IntelliJ IDEA <span class="bold">at all</span>, and please do not commit the project structure for any other IDE without first discussing it in the developer <a href="//java.nicholaswilliams.net/forums" target="_blank">forum</a>.<br />
